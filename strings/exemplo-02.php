<?php 
	
	$nome = "thiago freitas";

	//Função que transforma todas as letras em maiusculas
	echo strtoupper($nome);
	echo "</br>";

	//Função que transforma todas as letras em minusculas
	echo strtolower($nome);
	echo "</br>";

	//Função que transforma apenas a primeira letra de cada palavra em maiusculo
	echo ucwords($nome);
	echo "</br>";

	//Função que transforma apenas a primeira letra da frase em maisculo
	echo ucfirst($nome);
	echo "</br>";

?>